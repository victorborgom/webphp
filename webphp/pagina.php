class Pagina {
 private $cap;
 private $contingut;
 private $peu;
 private $menu;

 public function __construct($text)
 {
 $this->cap=new Caps();
 $this->menu=new Menu();
 $this->contingut=new Continguts();
 $this->peu=new Peus($text);
 }
 
  public function inserCap($text1, $text2)
 {
 $this->cap->insertarCap($text1, $text2);
 }
 
 public function inserCont($text3)
 {
 $this->contingut->insertarContenidos($text3);
 }
 
 public function inserMenu($text4, $text5)
 {
 $this->menu->insertarMenu($text4, $text5);
 }
 
 public function Mostrar()
 {
 $this->cap->Mostrar();
 $this->menu->Mostrar();
 $this->contingut->Mostrar();
 $this->peu->Mostrar();
 }
 
}

